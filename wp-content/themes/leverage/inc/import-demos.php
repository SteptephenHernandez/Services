if ( ! function_exists( 'leverage_after_import_demos' ) ) :
    function leverage_after_import_demos( $selected_import ) {

		if ( $selected_import['import_file_name'] === 'Inner Pages' ) {
			$blog = get_page_by_title( 'Our Blog' );
			update_option( 'page_for_posts', $blog->ID );

		} else {
			$page = get_page_by_title( $selected_import['import_file_name'] );
			update_option( 'page_on_front', $page->ID );
			update_option( 'show_on_front', 'page' );
		}
    }
    add_action( 'pt-ocdi/after_import', 'leverage_after_import_demos' );
endif;

